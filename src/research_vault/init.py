"""init.py — `rv init` — scaffold a fresh Research Vault instance.

When to use: ``rv init [<dir>]`` to scaffold a new multi-project Research Vault
instance in the given directory (or the current directory). Creates the instance
root with config, control files, task dirs, doctrine, notes root (OKF type dirs),
and the two canned demo projects (demo-research + demo-litreview).

Multi-repo topology note:
  The `examples/` demo projects are IN-REPO subdirs for zero-setup demonstration.
  Their source_dir points in-repo for the demo. A REAL project is a separate git
  repo at an external source_dir, registered by `rv project add`.
  `rv init` scaffolds the INSTANCE ROOT — it does NOT scaffold new projects as
  subdirs (that would re-introduce the monorepo pattern; use `rv project add` +
  the deferred `rv project new` capstone for real projects).

Stdlib only. No external deps.
"""
from __future__ import annotations

import argparse
import importlib.resources
import json
import shutil
import sys
from pathlib import Path


# ---------------------------------------------------------------------------
# Config template
# ---------------------------------------------------------------------------

_CONFIG_TEMPLATE = """\
# research_vault.toml — Research Vault instance configuration
# Generated by `rv init`. Edit to add real projects and customise paths.
#
# Multi-repo topology: real projects are SEPARATE repos registered via `rv project add`.
# The demo-research and demo-litreview entries below are in-repo examples only.
#
# See QUICKSTART.md for the next steps.

instance_root = "{instance_root}"
notes_root = "{notes_root}"
state_dir = "{state_dir}"
agents_dir = "{agents_dir}"
tasks_dir = "{tasks_dir}"
control_dir = "{control_dir}"

[adapters]
notifier = "file"
backend = "local"
secrets = "env"

# ── Demo projects (in-repo subdirs for zero-setup demonstration) ─────────────
# These point into the examples/ directory for the demo loops.
# Add REAL projects below as separate repos via `rv project add`.

[projects.demo-research]
source_dir = "{demo_research_dir}"
tasks_dir = "{tasks_dir}/demo-research"
control_file = "{control_dir}/demo-research.md"

[projects.demo-litreview]
source_dir = "{demo_litreview_dir}"
tasks_dir = "{tasks_dir}/demo-litreview"
control_file = "{control_dir}/demo-litreview.md"

# ── To add a real project ────────────────────────────────────────────────────
# Either run:
#   rv project add my-project /absolute/path/to/my-project-repo
#
# Or add manually:
# [projects.my-project]
# source_dir = "/absolute/path/to/my-project-repo"
"""

