def _pkg_data() -> "importlib.resources.abc.Traversable":
    """Return the package data root Traversable (src/research_vault/data/).

    Using ``importlib.resources.files()`` makes the lookup package-relative,
    so it works correctly from a wheel install, an editable install, or a
    zipimport — there is no ``__file__``-based repo-root path.
    """
    return importlib.resources.files("research_vault") / "data"


# ---------------------------------------------------------------------------
# Core init logic
# ---------------------------------------------------------------------------

def cmd_init_in_dir(target_dir: str) -> int:
    """Scaffold a fresh Research Vault instance in target_dir.

    Returns 0 on success, 1 on error.
    This is the programmatic entrypoint (also used by tests).
    """
    import datetime

    target = Path(target_dir).expanduser().resolve()

    # Guard: refuse to overwrite an existing instance
    config_path = target / "research_vault.toml"
    if config_path.exists():
        print(
            f"rv init: {config_path} already exists. "
            "Research Vault is already initialised here. "
            "Remove research_vault.toml to re-initialise.",
            file=sys.stderr,
        )
        return 1

    if not target.exists():
        target.mkdir(parents=True)

    print(f"Initialising Research Vault instance in: {target}")

    # ── Resolve all paths ────────────────────────────────────────────────────
    notes_root = target / "notes"
    state_dir = target / "state"
    agents_dir = target / ".agents"
    tasks_dir = target / "tasks"
    control_dir = target / "control"
    examples_dir = target / "examples"
    demo_research_dir = examples_dir / "demo-research"
    demo_litreview_dir = examples_dir / "demo-litreview"

    # ── Create directory structure ───────────────────────────────────────────
    for d in (
        notes_root,
        state_dir,
        agents_dir,
        tasks_dir / "demo-research",
        tasks_dir / "demo-litreview",
        control_dir,
        examples_dir,
        demo_research_dir,
        demo_litreview_dir,
    ):
        d.mkdir(parents=True, exist_ok=True)
        print(f"  created: {d.relative_to(target)}/")

    # ── Create OKF note type dirs ────────────────────────────────────────────
    scaffold_okf_dirs(notes_root)
    for note_type in sorted({"experiments", "findings", "methods", "literature", "concepts", "mocs"}):
        print(f"  created: notes/{note_type}/")

    # ── Write research_vault.toml ────────────────────────────────────────────
    today_str = datetime.date.today().isoformat()
    config_text = _CONFIG_TEMPLATE.format(
        instance_root=str(target),
        notes_root=str(notes_root),
        state_dir=str(state_dir),
        agents_dir=str(agents_dir),
        tasks_dir=str(tasks_dir),
        control_dir=str(control_dir),
        demo_research_dir=str(demo_research_dir),
        demo_litreview_dir=str(demo_litreview_dir),
    )
    config_path.write_text(config_text, encoding="utf-8")
    print(f"  created: research_vault.toml")

    # ── Write DEVLOG.md ──────────────────────────────────────────────────────
    devlog_text = _DEVLOG_TEMPLATE.format(
        today=today_str,
        instance_root=str(target),
    )
    (target / "DEVLOG.md").write_text(devlog_text, encoding="utf-8")
    print(f"  created: DEVLOG.md")

    # ── Write architecture.md ────────────────────────────────────────────────
    arch_text = _ARCHITECTURE_TEMPLATE.format(instance_root=str(target))
    (target / "architecture.md").write_text(arch_text, encoding="utf-8")
    print(f"  created: architecture.md")

    # ── Write control files ──────────────────────────────────────────────────
    for project in ("demo-research", "demo-litreview"):
        ctrl_text = _CONTROL_TEMPLATE.format(project=project)
        (control_dir / f"{project}.md").write_text(ctrl_text, encoding="utf-8")
        print(f"  created: control/{project}.md")

    # ── Copy QUICKSTART.md from package data (zipimport-safe) ───────────────
    pkg_data = _pkg_data()
    quickstart_dst = target / "QUICKSTART.md"
    with importlib.resources.as_file(pkg_data / "templates" / "QUICKSTART.md") as qs_src:
        if not qs_src.is_file():
            raise RuntimeError(
                "Package data missing: data/templates/QUICKSTART.md. "
                "The wheel is incomplete — reinstall research-vault."
            )
        shutil.copy2(qs_src, quickstart_dst)
    print(f"  created: QUICKSTART.md")

    # ── Copy doctrine/ from package data ─────────────────────────────────────
    doctrine_dst = target / "doctrine"
    with importlib.resources.as_file(pkg_data / "doctrine") as doctrine_src:
        if not doctrine_src.is_dir():
            raise RuntimeError(
                "Package data missing: data/doctrine/. "
                "The wheel is incomplete — reinstall research-vault."
            )
        shutil.copytree(str(doctrine_src), str(doctrine_dst), dirs_exist_ok=True)
    print(f"  created: doctrine/ ({_count_files(doctrine_dst)} files)")

    # ── Copy example loop manifests from package data ────────────────────────
    with importlib.resources.as_file(pkg_data / "examples" / "demo-research") as dr_src:
        if not dr_src.is_dir():
            raise RuntimeError(
                "Package data missing: data/examples/demo-research/. "
                "The wheel is incomplete — reinstall research-vault."
            )
        shutil.copytree(str(dr_src), str(demo_research_dir), dirs_exist_ok=True)
    print(f"  created: examples/demo-research/ ({_count_files(demo_research_dir)} files)")

    with importlib.resources.as_file(pkg_data / "examples" / "demo-litreview") as dl_src:
        if not dl_src.is_dir():
            raise RuntimeError(
                "Package data missing: data/examples/demo-litreview/. "
                "The wheel is incomplete — reinstall research-vault."
            )
        shutil.copytree(str(dl_src), str(demo_litreview_dir), dirs_exist_ok=True)
    print(f"  created: examples/demo-litreview/ ({_count_files(demo_litreview_dir)} files)")

    # ── SR-CCB: Scaffold CLAUDE.md (the Alfred hub-bootstrap) ────────────────
    # Makes a fresh `claude` session become Alfred, the hub.
    # Loaded via importlib.resources (zipimport-safe, same pattern as QUICKSTART).
    claude_md_dst = target / "CLAUDE.md"
    with importlib.resources.as_file(pkg_data / "templates" / "CLAUDE.md.tmpl") as tmpl_src:
        if not tmpl_src.is_file():
            raise RuntimeError(
                "Package data missing: data/templates/CLAUDE.md.tmpl. "
                "The wheel is incomplete — reinstall research-vault."
            )
        shutil.copy2(str(tmpl_src), str(claude_md_dst))
    print(f"  created: CLAUDE.md")

    # ── SR-CCB: Create .claude/agents/ dir (CC session-start requirement) ────
    # CC's file watcher registers .claude/agents/ only if it exists when the
    # session opens.  Create it now (even before populating) so a bare
    # `rv init && claude` works without a manual restart.
    dot_claude_agents = target / ".claude" / "agents"
    dot_claude_agents.mkdir(parents=True, exist_ok=True)
    print(f"  created: .claude/agents/")

    # ── SR-CCB: Auto-run build-agents --target claude-code ───────────────────
    # Populate .claude/agents/<role>.md with CC-format subagent files so the
    # crew is discoverable immediately (zero extra commands for the adopter).
    #
    # IMPORTANT — why we do NOT use load_config() here:
    #   The rv CLI calls load_config() at dispatch time (cli.py: cfg = load_config())
    #   to resolve instance-level plugin verbs.  That call populates the module-level
    #   _CACHE with a stale config — typically the default (no projects, CWD as
    #   instance_root) because `rv init myvault` is run from the PARENT directory
    #   before the new research_vault.toml even exists.  A subsequent load_config()
    #   call here would hit that stale cache regardless of any env-var override, and
    #   the auto-build would write files to the WRONG instance_root.
    #
    #   Fix: construct Config directly from the TOML we just wrote, bypassing the
    #   module-level cache entirely.  This is immune to cache state and always reads
    #   exactly what we persisted above.
    from .config import (
        Config, _load_toml, _expand_paths, _default_config, _merge, reset_config_cache,
    )
    from .build_agents import cmd_build as _cmd_build_agents
    from .project import DEFAULT_ROSTER as _DEFAULT_ROSTER
    try:
        _defaults = _default_config()
        _raw = _load_toml(config_path)
        _merged = _merge(_defaults, _raw)
        # instance_root is written as an absolute path in the TOML by _CONFIG_TEMPLATE;
        # fall back to `target` if somehow absent.
        _instance_root = Path(_merged.get("instance_root", str(target)))
        _merged = _expand_paths(_merged, _instance_root)
        _cfg = Config(_merged, config_file=config_path)
        # Reset the module-level cache so subsequent rv commands in the same process
        # reload from the new instance's config rather than any stale pre-init cache.
        reset_config_cache()

        _rc = _cmd_build_agents(cfg=_cfg, target="claude-code")
        if _rc != 0:
            print(
                "rv init: WARNING — build-agents --target claude-code returned non-zero. "
                "Run `rv build-agents --target claude-code` manually to populate .claude/agents/.",
                file=sys.stderr,
            )
        # Post-build assertion (Argus hardening): verify the expected agent files actually
        # exist.  A silent zero-exit with 0 files is more dangerous than a loud failure.
        _agents_dir = _instance_root / ".claude" / "agents"
        _present = list(_agents_dir.glob("*.md")) if _agents_dir.is_dir() else []
        _expected_count = len(_DEFAULT_ROSTER) + 1  # DEFAULT_ROSTER roles + architect
        if len(_present) < _expected_count:
            _missing = _expected_count - len(_present)
            print(
                f"rv init: ERROR — build-agents wrote {len(_present)} agent file(s) but "
                f"{_expected_count} were expected ({_missing} missing). "
                "The crew is incomplete. "
                "Run `rv build-agents --target claude-code` manually to investigate.",
                file=sys.stderr,
            )
            return 1
    except Exception as exc:
        print(
            f"rv init: WARNING — could not auto-run build-agents (claude-code): {exc}. "
            "Run `rv build-agents --target claude-code` manually to populate .claude/agents/.",
            file=sys.stderr,
        )

    print()
    print("Research Vault instance initialised.")
    print()
    print("Open `claude` in this directory to start — you'll be Alfred, the hub.")
    print("The crew is stood up as subagents in .claude/agents/")
    print()
    print("Next steps:")
    print("  1. rv check                     — verify prerequisites")
    print("  2. rv compute init              — DECLARE your compute (scaffold manifest)")
    print("     rv doctor                    — DISCOVER capabilities per declared backend")
    print("     rv compute show              — VERIFY the merged recipe")
    print("  3. rv dag run examples/demo-research/research-loop.json  — start the research loop demo")
    print("  4. rv dag run examples/demo-litreview/lit-review-loop.json  — start the lit-review demo")
    print("  5. rv project add <slug> <path> — add a real project")
    print()
    print("See QUICKSTART.md for a full walkthrough.")
    return 0


def _count_files(path: Path) -> int:
    """Count files recursively in a directory."""
    return sum(1 for _ in path.rglob("*") if _.is_file())


