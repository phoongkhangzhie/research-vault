"""init.py — `rv init` — scaffold a fresh Research Vault instance.

When to use: ``rv init [<dir>]`` to scaffold a new multi-project Research Vault
instance in the given directory (or the current directory). Creates the instance
root with config, control files, task dirs, doctrine, notes root (OKF type dirs),
and the two canned demo projects (demo-research + demo-litreview).

SR-CCB additions:
  ``rv init`` also scaffolds the Claude Code binding so a bare
  ``rv init && claude`` boots Alfred + the full crew:
    1. Writes ``CLAUDE.md`` at the instance root (the hub-bootstrap: tells a
       fresh ``claude`` session that it IS Alfred, the hub).
    2. Creates an empty ``.claude/agents/`` dir (CC's session-start requirement:
       the watcher only registers dirs that exist before the session opens).
    3. Auto-runs ``build-agents --target claude-code`` to populate
       ``.claude/agents/<role>.md`` with CC-format subagent files for the
       DEFAULT_ROSTER (5 roles) + architect = 6 files (charter + role doctrine,
       vault-level — no per-project lens; SR-LENS-RM).

Multi-repo topology note:
  The `examples/` demo projects are shipped inside the package under
  ``src/research_vault/data/examples/`` and copied on ``rv init``.
  A REAL project is a separate git repo at an external source_dir, registered
  by ``rv project add``.
  ``rv init`` scaffolds the INSTANCE ROOT — it does NOT scaffold new projects as
  subdirs (that would re-introduce the monorepo pattern; use ``rv project add`` +
  the deferred ``rv project new`` capstone for real projects).

Package data layout (SR-PKG):
  All files copied by ``rv init`` live under ``src/research_vault/data/`` inside
  the wheel.  They are loaded via ``importlib.resources`` + ``as_file()`` so the
  copy works from a regular wheel install AND from a zipped wheel (zipimport-safe).
  There are NO ``__file__``-based fallbacks — a missing data file is a HARD ERROR,
  not a silent skeleton (charter §2: surface, never silently degrade).

Stdlib only. No external deps.
"""
